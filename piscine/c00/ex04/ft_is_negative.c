#include <unistd.h>

void	ft_is_negative(int n)
{
	char	p;
	char	neg;

	p = 'P';
	neg = 'N';
	if (n < 0)
	{
		write(1, &neg, 1);
	}
	else
	{
		write(1, &p, 1);
	}
}

